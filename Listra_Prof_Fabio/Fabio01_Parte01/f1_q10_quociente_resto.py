# ENTRADA
print('Entre com dois números inteiros')
numero1 = int(input('Digite o 1º numero: '))
numero2 = int(input('Digite o 2º numero: '))

# PROCESSAMENTO
quociente = numero1 // numero2
resto = numero1 % numero2

# SAÍDA
print('')
print('O quociente da divisão do 1º pelo 2º número é: ', quociente)
print('O resto da divisão do 1º pelo 2º número é: ', resto)
print('')