# ENTRADA
print('A seguir, digite 3 números:')
numero1 = float(input('1º número: '))
numero2 = float(input('2º número: '))
numero3 = float(input('3º número: '))

# PROCESSAMENTO
soma = numero1 + numero2
subtracao = numero2 - numero3

# SAÍDA
print('')
print('A soma entre os dois primeiros números é: {}'.format(soma))
print('A diferença entre o 2º e 3º número é: {}'.format(subtracao))
print('')
